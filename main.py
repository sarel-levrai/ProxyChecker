def check_proxy(proxy):
    global working, dead

    try:
        proxies = {
            "http": f"http://{proxy}",
            "https": f"http://{proxy}"
        }

        r = requests.get(
            "http://httpbin.org/ip",
            proxies=proxies,
            timeout=2
        )

        if r.status_code == 200:
            print(Colors.green + f"[+] {proxy}")
            working_file.write(proxy + "\n")
            working += 1
            return

    except:
        pass

    print(Colors.red + f"[-] {proxy}")
    dead_file.write(proxy + "\n")
    dead += 1


